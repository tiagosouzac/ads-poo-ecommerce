    private void queryButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_queryButtonActionPerformed
        int selectedIndex = supplierCmb.getSelectedIndex();
        if (selectedIndex >= 0) {
            UUID selectedSupplierId = supplierIdMap.get(selectedIndex);
            Supplier selectedSupplier = supplierController.getSuppliers().stream()
                    .filter(supplier -> supplier.getId().equals(selectedSupplierId))
                    .findFirst()
                    .orElse(null);

            if (selectedSupplier != null) {
                nameTxt.setText(selectedSupplier.getName() != null ? selectedSupplier.getName() : "");
                cnpjTxt.setText(selectedSupplier.getCnpj() != null ? selectedSupplier.getCnpj() : "");
                phoneTxt.setText(selectedSupplier.getPhone() != null ? selectedSupplier.getPhone() : "");
                emailTxt.setText(selectedSupplier.getEmail() != null ? selectedSupplier.getEmail() : "");

                Address address = addressController.findAddressByAddressableId(selectedSupplier.getId());
                if (address != null) {
                    zipCodeTxt.setText(address.getZipCode() != null ? address.getZipCode() : "");
                    streetTxt.setText(address.getStreet() != null ? address.getStreet() : "");
                    numberTxt.setText(address.getNumber() != null ? address.getNumber() : "");
                    neighborhoodTxt.setText(address.getNeighborhood() != null ? address.getNeighborhood() : "");
                    complementTxt.setText(address.getComplement() != null ? address.getComplement() : "");
                    cityTxt.setText(address.getCity() != null ? address.getCity() : "");
                    stateTxt.setText(address.getState() != null ? address.getState() : "");
                    countryTxt.setText(address.getCountry() != null ? address.getCountry() : "");
                } else {
                    JOptionPane.showMessageDialog(this, "Endereço do fornecedor não encontrado.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Fornecedor selecionado não encontrado.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum fornecedor selecionado.");
        }
    }//GEN-LAST:event_queryButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        try {
            int selectedIndex = supplierCmb.getSelectedIndex();
            if (selectedIndex < 0) {
                JOptionPane.showMessageDialog(this, "Selecione um fornecedor para atualizar.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            UUID selectedSupplierId = supplierIdMap.get(selectedIndex);
            Supplier supplier = supplierController.getSupplierById(selectedSupplierId);

            if (supplier == null) {
                JOptionPane.showMessageDialog(this, "Fornecedor não encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String strName = nameTxt.getText().trim();
            String strCnpj = cnpjTxt.getText().trim();
            String strEmail = emailTxt.getText().trim();
            String strPhone = phoneTxt.getText().trim();
            String strStreet = streetTxt.getText().trim();
            String strNumber = numberTxt.getText().trim();
            String strNeighborhood = neighborhoodTxt.getText().trim();
            String strComplement = complementTxt.getText().trim();
            String strCity = cityTxt.getText().trim();
            String strState = stateTxt.getText().trim();
            String strCountry = countryTxt.getText().trim();
            String strZipCode = zipCodeTxt.getText().trim();

            if (strName.isEmpty() || strEmail.isEmpty() || strCnpj.isEmpty() || strPhone.isEmpty() || strStreet.isEmpty() || strNumber.isEmpty() ||
                    strNeighborhood.isEmpty() || strCity.isEmpty() || strState.isEmpty() || strCountry.isEmpty() || strZipCode.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha todos os campos, exceto o complemento, que é opcional.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Address address = supplier.getAddress();
            if (address == null) {
                address = new Address();
            }

            address.setStreet(strStreet);
            address.setNumber(strNumber);
            address.setNeighborhood(strNeighborhood);
            address.setComplement(strComplement);
            address.setCity(strCity);
            address.setState(strState);
            address.setCountry(strCountry);
            address.setZipCode(strZipCode);
            address.setAddressable(supplier);

            supplier.setName(strName);
            supplier.setCnpj(strCnpj);
            supplier.setEmail(strEmail);
            supplier.setPhone(strPhone);
            supplier.setAddress(address);

            supplierController.saveSupplier(supplier);
            JOptionPane.showMessageDialog(this, "Fornecedor atualizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Falha ao tentar atualizar fornecedor.", "Erro", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_editButtonActionPerformed

