    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            String strName = nameTxt.getText().trim();
            String strDescription = descriptionTxt.getText().trim();
            String strPrice = priceTxt.getText().trim();
            String strStock = stockTxt.getText().trim();

            int selectedCategoryIndex = categoryCmb.getSelectedIndex();
            int selectedBrandIndex = brandCmb.getSelectedIndex();
            int selectedSupplierIndex = supplierCmb.getSelectedIndex();

            if (strName.isEmpty() || strDescription.isEmpty() || strPrice.isEmpty() || strStock.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha todos os campos.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (selectedCategoryIndex == -1 || selectedBrandIndex == -1 || selectedSupplierIndex == -1) {
                JOptionPane.showMessageDialog(this, "Selecione uma categoria, marca e fornecedor.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            UUID selectedCategoryId = categoryIdMap.get(selectedCategoryIndex);
            UUID selectedBrandId = brandIdMap.get(selectedBrandIndex);
            UUID selectedSupplierId = supplierIdMap.get(selectedSupplierIndex);

            Category selectedCategory = categoryController.getCategories().stream()
                    .filter(c -> c.getId().equals(selectedCategoryId))
                    .findFirst().orElse(null);
            Brand selectedBrand = brandController.getBrands().stream()
                    .filter(b -> b.getId().equals(selectedBrandId))
                    .findFirst().orElse(null);
            Supplier selectedSupplier = supplierController.getSuppliers().stream()
                    .filter(s -> s.getId().equals(selectedSupplierId))
                    .findFirst().orElse(null);

            BigDecimal price = new BigDecimal(strPrice);
            int stock = Integer.parseInt(strStock);
            Product product = new ProductBuilder()
                    .name(strName)
                    .description(strDescription)
                    .price(price)
                    .stock(stock)
                    .category(selectedCategory)
                    .brand(selectedBrand)
                    .supplier(selectedSupplier).build();

            productController.saveProduct(product);

            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Falha ao tentar cadastrar o produto.", "Erro", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private final Map<Integer, UUID> productIdMap = new HashMap<>();
